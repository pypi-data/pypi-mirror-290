import re as _re
import collections as _collections

def get_sus(text: str):
    regex = _re.compile(r'imposter|impostor|suspicious|sus|amongus|among|amogus|us', _re.I)
    mo = regex.findall(text)
    named = _collections.namedtuple('ඞ', ('sus'+str(i) for i in range(len(mo))))
    return named(*mo)

def capitalize_sus(text: str) -> str:
    sus = ['imposter', 'impostor', 'suspicious', 'amongus', 'amogus', 'among', 'sus', 'us']
    for i in sus:
        text = text.replace(i, i.upper())
    return text

def is_sus(text: str) -> bool:
    regex = _re.compile(r'imposter|impostor|suspicious|sus|amongus|among|amogus|us', _re.I)
    return bool(regex.findall(text))

def susify(text: str) -> str:
    result = ''
    for i in text:
        if i != ' ' and i != '\n' and i != '\t':
            result += 'ඞ'
        else:
            result += i
    return result

def sus_tuple(*args):
    named = _collections.namedtuple('ඞ', ('sus'+str(i) for i in range(len(args))))
    return named(*args)

